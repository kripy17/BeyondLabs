function mergeSettings(raw = {}) {
  return {
    ...DEFAULT_SETTINGS,
    ...raw,
    appearance: { ...DEFAULT_SETTINGS.appearance, ...(raw.appearance || {}) },
    general: { ...DEFAULT_SETTINGS.general, ...(raw.general || {}) },
    safety: { ...DEFAULT_SETTINGS.safety, ...(raw.safety || {}) },
    backend: { ...DEFAULT_SETTINGS.backend, ...(raw.backend || {}) },
  }
}

function loadSettings() {
  try {
    const parsed = JSON.parse(localStorage.getItem(SETTINGS_KEY) || "null")
    return { settings: { ...mergeSettings(parsed || DEFAULT_SETTINGS), appearance: { ...mergeSettings(parsed || DEFAULT_SETTINGS).appearance, theme: loadThemePreference() } }, warning: "" }
  } catch {
    return {
      settings: { ...DEFAULT_SETTINGS, appearance: { ...DEFAULT_SETTINGS.appearance, theme: loadThemePreference() } },
      warning: "Stored settings were unreadable. Defaults are shown until you save again.",
    }
  }
}

function saveSettings(value) {
  const next = mergeSettings({ ...value, updatedAt: new Date().toISOString() })
  localStorage.setItem(SETTINGS_KEY, JSON.stringify(next))
  saveThemePreference(next.appearance.theme)
  return next
}

function applyUiPreferences(settings) {
  const root = document.documentElement
  root.dataset.density = settings.appearance.density
  root.dataset.accent = settings.appearance.accent
  root.dataset.motion = settings.appearance.reduceMotion ? "reduced" : "normal"
  root.classList.toggle("ba-compact-mode", Boolean(settings.general.compactMode))
}

function safeRead(key) {
  try {
    return localStorage.getItem(key)
  } catch {
    return null
  }
}

function roughSize(value = "") {
  const bytes = new Blob([value]).size
  if (bytes > 1024 * 1024) return `${(bytes / 1024 / 1024).toFixed(2)} MB`
  if (bytes > 1024) return `${(bytes / 1024).toFixed(1)} KB`
  return `${bytes} B`
}

function storageSummary() {
  return STORAGE_ITEMS.map((item) => {
    const values = item.keys.map((key) => safeRead(key)).filter((value) => value !== null)
    const size = values.reduce((sum, value) => sum + new Blob([value]).size, 0)
    return { ...item, exists: values.length > 0, size: roughSize("x".repeat(size)), keyCount: values.length }
  })
}

function rawStorageRows() {
  return Object.keys(localStorage)
    .filter((key) => key.startsWith("beyondarch"))
    .sort()
    .map((key) => ({ key, size: roughSize(safeRead(key) || "") }))
}

function downloadJson(filename, payload) {
  const blob = new Blob([JSON.stringify(payload, null, 2)], { type: "application/json" })
  const url = URL.createObjectURL(blob)
  const link = document.createElement("a")
  link.href = url
  link.download = filename
  link.click()
  URL.revokeObjectURL(url)
}

function parseImport(text) {
  try {
    const parsed = JSON.parse(text)
    if (!parsed || typeof parsed !== "object") return { error: "Import must be a JSON object." }
    if (parsed.type !== "beyondarch_settings_export") return { error: "This is not a BeyondArch settings export." }
    if (!parsed.data || typeof parsed.data !== "object") return { error: "Export data is missing." }
    return { parsed }
  } catch (error) {
    return { error: `Invalid JSON: ${error.message}` }
  }
}

function normalizeBaseUrl(value) {
  return String(value || "http://127.0.0.1:8000").replace(/\/+$/, "")
}

