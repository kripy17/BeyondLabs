function SelectField({ label, description, value, options, onChange }) {
  const [open, setOpen] = useState(false)
  const [activeIndex, setActiveIndex] = useState(0)
  const ref = useRef(null)
  const selectedIndex = Math.max(0, options.findIndex((option) => option.value === value))
  const selected = options[selectedIndex] || options[0]

  useEffect(() => {
    if (!open) return undefined
    function closeOnOutside(event) {
      if (ref.current && !ref.current.contains(event.target)) setOpen(false)
    }
    document.addEventListener("pointerdown", closeOnOutside)
    return () => document.removeEventListener("pointerdown", closeOnOutside)
  }, [open])

  function choose(option) {
    onChange(option.value)
    setOpen(false)
  }

  function handleKeyDown(event) {
    if (event.key === "Escape") setOpen(false)
    if (event.key === "Enter" || event.key === " ") {
      event.preventDefault()
      if (!open) setOpen(true)
      else choose(options[activeIndex])
    }
    if (event.key === "ArrowDown" || event.key === "ArrowUp") {
      event.preventDefault()
      if (!open) setOpen(true)
      else setActiveIndex((current) => (current + (event.key === "ArrowDown" ? 1 : -1) + options.length) % options.length)
    }
  }

  return (
    <div className="relative rounded-xl border border-white/10 bg-black/40 p-3" ref={ref}>
      <span className="block text-sm font-bold text-zinc-100">{label}</span>
      {description && <span className="mt-1 block text-xs leading-5 text-zinc-400">{description}</span>}
      <button type="button" className="mt-2 flex w-full items-center justify-between rounded-lg border border-white/10 bg-black/50 px-3 py-2 text-left text-sm text-zinc-100 outline-none focus:border-cyan-400" aria-haspopup="listbox" aria-expanded={open} onClick={() => setOpen((value) => !value)} onKeyDown={handleKeyDown}>
        <span>
          <span className="block font-bold">{selected?.label}</span>
          {selected?.description && <span className="block text-xs text-zinc-400">{selected.description}</span>}
        </span>
        <ChevronDown className={`h-4 w-4 text-zinc-400 transition ${open ?"rotate-180" : ""}`} />
      </button>
      {open && (
        <div className="absolute left-3 right-3 top-[calc(100%-0.5rem)] z-30 overflow-hidden rounded-xl border border-white/10 bg-black/40 shadow-2xl" role="listbox">
          {options.map((option, index) => (
            <button key={option.value} type="button" role="option" aria-selected={option.value === value} className={`flex w-full items-start justify-between gap-3 px-3 py-2 text-left text-sm transition ${index === activeIndex ? "bg-cyan-500/15 text-cyan-100" : "text-zinc-200 hover:bg-black/40"}`} onMouseEnter={() => setActiveIndex(index)} onClick={() => choose(option)}>
              <span>
                <span className="block font-bold">{option.label}</span>
                {option.description && <span className="block text-xs text-zinc-400">{option.description}</span>}
              </span>
              {option.value === value && <Check className="mt-0.5 h-4 w-4 text-cyan-300" />}
            </button>
          ))}
        </div>
      )}
    </div>
  )
}

